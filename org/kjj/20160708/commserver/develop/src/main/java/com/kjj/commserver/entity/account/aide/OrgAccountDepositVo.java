package com.kjj.commserver.entity.account.aide;

import java.math.BigDecimal;

import com.kjj.commserver.entity.account.OrgAccountDeposit;

public class OrgAccountDepositVo extends OrgAccountDeposit {
	/** 手机号 */
	private String mobilePhone;
	
	/** 可用金额 = 可用可提现金额 +可用不可提现金额  */
    private BigDecimal canUseAmount;
	
	public String getMobilePhone() {
		return mobilePhone;
	}

	public void setMobilePhone(String mobilePhone) {
		this.mobilePhone = mobilePhone;
	}

	public BigDecimal getCanUseAmount() {
		return canUseAmount;
	}

	public void setCanUseAmount(BigDecimal canUseAmount) {
		this.canUseAmount = canUseAmount;
	}
}