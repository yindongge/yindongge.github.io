package com.kjj.commserver.entity.discount.aide;

import java.util.List;

import com.kjj.commserver.entity.discount.OrgCoupon;
import com.kjj.commserver.entity.discount.OrgReachCoupon;

public class OrgCouponVo extends OrgCoupon {

	/** 优惠券在该店是否可用 */
	private Boolean markcurrentShop = true;
	
	/** 可用此优惠券店铺的店铺名称 */
	private List<String> shopNames;
	
	/** 满减满送赠送优惠券 */
	private OrgReachCoupon orgReachCoupon;

	public Boolean getMarkcurrentShop() {
		return markcurrentShop;
	}

	public void setMarkcurrentShop(Boolean markcurrentShop) {
		this.markcurrentShop = markcurrentShop;
	}

	public List<String> getShopNames() {
		return shopNames;
	}

	public void setShopNames(List<String> shopNames) {
		this.shopNames = shopNames;
	}

	public OrgReachCoupon getOrgReachCoupon() {
		return orgReachCoupon;
	}

	public void setOrgReachCoupon(OrgReachCoupon orgReachCoupon) {
		this.orgReachCoupon = orgReachCoupon;
	}

	
	

}