package com.kjj.commserver.entity.goods.aide;

import java.util.ArrayList;
import java.util.List;

import com.kjj.commserver.entity.goods.OrgClass;
import com.kjj.commserver.entity.goods.OrgProductType;

public class OrgClassVo extends OrgClass {
	/**子分类集合*/
	private List<OrgClass> childrenList = new ArrayList<OrgClass>();
	
	/**商品类型*/
	private OrgProductType productType;
	
	/**sku数量*/
	private Long skuNum = 0L;

	public List<OrgClass> getChildrenList() {
		return childrenList;
	}

	public void setChildrenList(List<OrgClass> childrenList) {
		this.childrenList = childrenList;
	}

	public OrgProductType getProductType() {
		return productType;
	}

	public void setProductType(OrgProductType productType) {
		this.productType = productType;
	}

	public Long getSkuNum() {
		return skuNum;
	}

	public void setSkuNum(Long skuNum) {
		this.skuNum = skuNum;
	}
	
}