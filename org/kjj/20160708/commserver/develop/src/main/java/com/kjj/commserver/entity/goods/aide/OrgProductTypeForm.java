package com.kjj.commserver.entity.goods.aide;

import java.util.List;

import com.kjj.commserver.entity.goods.OrgProductType;

public class OrgProductTypeForm extends OrgProductType{
	/**销售规格id集合*/
	private List<Integer> specId;
	/**属性排序集合*/
	private List<Integer> propertyOrder;
	/**属性名称集合*/
	private List<String> propertyName;
	/**录入方式集合*/
	private List<Integer> propertyInputType;
	/**是否必选集合*/
	private List<Byte> propertySelect;
	/**属性值集合*/
	private List<String> propertyValue;
	public List<Integer> getSpecId() {
		return specId;
	}
	public void setSpecId(List<Integer> specId) {
		this.specId = specId;
	}
	public List<Integer> getPropertyOrder() {
		return propertyOrder;
	}
	public void setPropertyOrder(List<Integer> propertyOrder) {
		this.propertyOrder = propertyOrder;
	}
	public List<String> getPropertyName() {
		return propertyName;
	}
	public void setPropertyName(List<String> propertyName) {
		this.propertyName = propertyName;
	}
	public List<Integer> getPropertyInputType() {
		return propertyInputType;
	}
	public void setPropertyInputType(List<Integer> propertyInputType) {
		this.propertyInputType = propertyInputType;
	}
	public List<Byte> getPropertySelect() {
		return propertySelect;
	}
	public void setPropertySelect(List<Byte> propertySelect) {
		this.propertySelect = propertySelect;
	}
	public List<String> getPropertyValue() {
		return propertyValue;
	}
	public void setPropertyValue(List<String> propertyValue) {
		this.propertyValue = propertyValue;
	}
	
	
}
