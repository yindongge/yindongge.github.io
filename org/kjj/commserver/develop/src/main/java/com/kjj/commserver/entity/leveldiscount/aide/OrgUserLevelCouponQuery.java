package com.kjj.commserver.entity.leveldiscount.aide;

import com.kjj.commserver.entity.leveldiscount.OrgUserLevelCoupon;

public class OrgUserLevelCouponQuery extends OrgUserLevelCoupon {
	
	/** 商品的唯一货号 */
    private String goodsSn;

    /** 商品的名称  */
    private String goodsName;
    
    /** 多个店铺Id*/
    private String cityShopIds;
    
    /* 商品是否删除*/
    private Byte isDelete;

	public String getCityShopIds() {
		return cityShopIds;
	}

	public Byte getIsDelete() {
		return isDelete;
	}

	public void setIsDelete(Byte isDelete) {
		this.isDelete = isDelete;
	}

	public void setCityShopIds(String cityShopIds) {
		this.cityShopIds = cityShopIds;
	}

	public String getGoodsSn() {
		return goodsSn;
	}

	public void setGoodsSn(String goodsSn) {
		this.goodsSn = goodsSn;
	}

	public String getGoodsName() {
		return goodsName;
	}

	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}
}