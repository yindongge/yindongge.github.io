package com.kjj.commserver.entity.account.aide;

import java.math.BigDecimal;

import com.kjj.commserver.entity.account.OrgAccountDepositRecords;

public class OrgAccountDepositRecordsVo extends OrgAccountDepositRecords {
	/** 会员名*/
	private String userName;
	
	/** 前台类型显示 */
	private String typeShow;
	
	/** 前台金额显示 */
	private BigDecimal amountShow;
	
	/** 交易人登录名*/
	private String traderName;
	
	/** 交易人手机号*/
	private String mobilePhone;
	
	/** 交易人姓名备注*/
	private String realName;
	
	public String getRealName() {
		return realName;
	}

	public void setRealName(String realName) {
		this.realName = realName;
	}

	public String getMobilePhone() {
		return mobilePhone;
	}

	public void setMobilePhone(String mobilePhone) {
		this.mobilePhone = mobilePhone;
	}

	public String getTraderName() {
		return traderName;
	}

	public void setTraderName(String traderName) {
		this.traderName = traderName;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getTypeShow() {
		return typeShow;
	}

	public void setTypeShow(String typeShow) {
		this.typeShow = typeShow;
	}

	public BigDecimal getAmountShow() {
		return amountShow;
	}

	public void setAmountShow(BigDecimal amountShow) {
		this.amountShow = amountShow;
	}
	
}